<h4>Estilo industrial</h4>
<p align="left">
El origen del estilo industrial y su evolución Fue a mediados del Siglo XX cuando este estilo se vio influenciado por el Art Deco, fusionando la calidez de la madera y la frialdad del acero.

Una combinación de materiales única que logró una gran admiración a nivel global. Durante la década de los 50 muchas fábricas neoyorquinas cerraron, pasando a convertirse en viviendas.

Fue en ellas donde nació este estilo de decoración tal y como lo conocemos hoy en día. Grandes espacios sin apenas divisiones, en los que los materiales se muestran como tal, sin ningún tipo de pretensión estética.

La luminosidad juega un papel clave en el estilo industrial. Las antiguas fábricas de Nueva York tenían grandes ventanales, por los que entraba una gran cantidad de luz natural, que fluía por toda la estancia.

Los muebles y piezas de decoración permiten lograr un estilo industrial en el hogar sin necesidad de vivir en un loft. Los muebles deben ser de tamaño grande, fabricados en madera natural sin tratar y/o metal.

Para las estanterías y otro espacios de almacenaje, las estanterías de metal son una gran opción</p>
